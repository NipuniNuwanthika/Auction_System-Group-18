const mongoose = require('mongoose');
const Schema = mongoose.Schema;

//create user Schema 
const UserSchema = new Schema({
  first_name: {
    type: String,
    required: true
  },
  last_name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  phone: {
    type: String,
    required: true
  },
  address: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  type: {
    type: String,
    required: true
  },

}, { timestamps: true, strict: false });


module.exports = mongoose.model('User', UserSchema);
